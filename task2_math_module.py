import math    # importing built-in math module

# taking input from user
num = float(input("Enter a number:"))

# calculation using math module 
square_root = math.sqrt(num)   # square root
logarithm = math.log(num)      # logarithm
sine = math.sin(num)           # sine 

# displaying the output 
print(f"square root of {num} is {square_root}")
print(f"logarithm value of {num} is {logarithm}")
print(f"sine value of {num} is {sine}")
